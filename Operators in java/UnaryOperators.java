public class UnaryOperators {

    // shatrudha.web.developer

    /*
     * Unary operators only two types
     * (a) Increment operetor
     * 1. Pre-Increment (++i)
     * 2. Post-Increment (i++)
     * 
     * (b) Decrement operator
     * 1. Pre-Decrement (--i)
     * 2. Post-Decrement (i--)
     */

    public static void main(String[] args) {

        // Increment Operators
        int x = 10;
        System.out.println(++x); // For pre-increment
        System.out.println(x++); // For post-increment

        // Decrement Operators
        System.out.println(--x); // For pre-decrement
        System.out.println(x--); // For post-decrement
    }
}
